export $(grep "^ID=" /etc/os-release)
ID=${ID//\"/}

case $ID in
centos) centos;;
debian|kali) debian;;
fedora) fedora;;
raspbian) raspbian;;
rhel) rhel;;
sles) rhel;;
ubuntu) ubuntu;;
*) echo "Sistema n√£o identificado";;
esac
